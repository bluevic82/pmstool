package com.tinhvan.model;

public class ScopeProject {
	private int project_id;
	private int scope_id;
	private int scope_project_id;
	public int getProject_id() {
		return project_id;
	}
	public void setProject_id(int project_id) {
		this.project_id = project_id;
	}
	public int getScope_id() {
		return scope_id;
	}
	public void setScope_id(int scope_id) {
		this.scope_id = scope_id;
	}
	public int getScope_project_id() {
		return scope_project_id;
	}
	public void setScope_project_id(int scope_project_id) {
		this.scope_project_id = scope_project_id;
	}
	
}
